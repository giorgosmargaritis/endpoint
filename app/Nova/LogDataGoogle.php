            Text::make('Data Received', 'data_received'),

            DateTime::make('Created At')
            ->displayUsing(fn ($value) => $value ? $value->format(config('connector.datetime_format')) : '')
            ->filterable(),

            DateTime::make('Updated At')
            ->displayUsing(fn ($value) => $value ? $value->format(config('connector.datetime_format')) : '')
            ->filterable(),
        ];
    }

